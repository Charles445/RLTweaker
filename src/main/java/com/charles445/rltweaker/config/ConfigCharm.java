	@Config.Name("Fix Charged Emerald Crash")
	@RLConfig.ImprovementsOnly("true")
	@RLConfig.RLCraftTwoEightTwo("true")
	@RLConfig.RLCraftTwoNine("true")
	public boolean fixChargedEmeraldCrash = true;
